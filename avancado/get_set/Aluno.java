package avancado.get_set;

public class Aluno {
    private String nome;
    private int idade;

    public String getNome(){
        return nome;
    }

    public void setNome(String name){
        nome = name;
    }

    public int getIdade(){
        return idade;
    }
    public void setIdade(int age){
        idade = age;
    }


}
